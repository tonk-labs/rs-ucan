/// Build error type for invocation builder.
#[derive(Debug, thiserror::Error)]
pub enum BuildError {
    /// Encoding error.
    #[error("Encoding error: {0}")]
    EncodingError(String),

    /// Signing error.
    #[error("Signing error: {0}")]
    SigningError(#[from] signature::Error),
}

/// Building methods that use async signing.
///
/// This impl block uses async signing via the [`Issuer`] trait (which is a `Signer`),
/// which works with both native `ed25519_dalek` signers and `WebCrypto` signers.
#[allow(clippy::mismatching_type_param_order)]
impl<D: Issuer> InvocationBuilder<D, D, D::Principal, D::Principal, Command, Vec<Cid>> {
    /// Builds the complete, signed [`Invocation`].
    ///
    /// Uses the issuer (set via `.issuer()`) to sign the invocation.
    /// The signing is performed asynchronously via [`Issuer::sign()`].
    ///
    /// # Errors
    ///
    /// * `BuildError::EncodingError` if encoding the payload fails
    /// * `BuildError::SigningError` if signing fails
    ///
    /// # Panics
    ///
    /// Panics if random number generator fails when generating a nonce.
    /// This will never happen if a nonce is provided, and is not recoverable
    /// because a broken RNG is a serious problem.
    ///
    /// # Example
    ///
    /// ```ignore
    /// let signer = Ed25519Signer::new(signing_key);
    /// let invocation = InvocationBuilder::new()
    ///     .issuer(signer)
    ///     .audience(audience_did)
    ///     .subject(subject_did)
    ///     .command(vec!["storage".into(), "read".into()])
    ///     .proofs(vec![delegation_cid])
    ///     .try_build()
    ///     .await?;
    /// ```
    #[allow(clippy::expect_used)]
    pub async fn try_build(self) -> Result<super::Invocation<D::Principal>, BuildError> {
        let payload: super::InvocationPayload<D::Principal> = super::InvocationPayload {
            issuer: self.issuer.principal().clone(),
            audience: self.audience,
            subject: self.subject,
            command: self.command,
            arguments: self.arguments,
            expiration: self.expiration,
            issued_at: self.issued_at,
            proofs: self.proofs,
            cause: self.cause,
            meta: self.meta,
            nonce: self
                .nonce
                .unwrap_or_else(|| Nonce::generate_16().expect("failed to generate nonce")),
        };

        let header: Varsig<
            <D::Principal as Verifier>::Algorithm,
            CborCodec,
            super::InvocationPayload<D::Principal>,
        > = Varsig::new(CborCodec);

        // Encode and sign the payload
        let encoded = header
            .encode(&payload)
            .map_err(|e| BuildError::EncodingError(e.to_string()))?;
        let sig = self
            .issuer
            .sign(&encoded)
            .await
            .map_err(BuildError::SigningError)?;

        let payload: EnvelopePayload<
            <D::Principal as Verifier>::Algorithm,
            super::InvocationPayload<D::Principal>,
        > = EnvelopePayload { header, payload };

        #[allow(clippy::type_complexity)]
        let envelope: Envelope<
            <D::Principal as Verifier>::Algorithm,
            super::InvocationPayload<D::Principal>,
            <<D::Principal as Verifier>::Algorithm as SignatureAlgorithm>::Signature,
        > = Envelope(sig, payload);

        let invocation: super::Invocation<D::Principal> = super::Invocation(envelope);

        Ok(invocation)
    }
}
