//! Typesafe builder for [`InvocationPayload`].

use crate::{
    codec::CborCodec,
    command::Command,
    crypto::nonce::Nonce,
    envelope::{Envelope, EnvelopePayload},
    issuer::Issuer,
    promise::Promised,
    sealed::{CommandOrUnset, IssuerOrUnset, PrincipalOrUnset, ProofsOrUnset},
    time::timestamp::Timestamp,
    unset::Unset,
};
use ipld_core::{cid::Cid, ipld::Ipld};
use std::{collections::BTreeMap, marker::PhantomData};
use varsig::{
    algorithm::SignatureAlgorithm,
    signature::{verifier::Verifier, Varsig},
};

/// Typesafe builder for [`Invocation`].
#[allow(private_bounds)]
#[derive(Default, Debug, Clone)]
pub struct InvocationBuilder<
    D: Issuer,
    Iss: IssuerOrUnset = Unset,
    Audience: PrincipalOrUnset = Unset,
    Subject: PrincipalOrUnset = Unset,
    Cmd: CommandOrUnset = Unset,
    Proofs: ProofsOrUnset = Unset,
> {
    /// Issuer of the invocation.
    pub issuer: Iss,

    /// Audience of the invocation.
    pub audience: Audience,

    /// Subject of the invocation (the resource or object being accessed).
    pub subject: Subject,

    /// Cmd being invoked.
    pub command: Cmd,

    /// The arguments for the Cmd.
    pub arguments: BTreeMap<String, Promised>,

    /// Proofs of the invocation.
    pub proofs: Proofs,

    /// Cause of the invocation.
    pub cause: Option<Cid>,

    /// Expiration time of the invocation.
    pub expiration: Option<Timestamp>,

    /// The time at which this invocation claims to have been issued.
    pub issued_at: Option<Timestamp>,

    /// Extensible metadata for the delegation.
    pub meta: BTreeMap<String, Ipld>,

    /// Nonce (will be autogenerated if left blank).
    pub nonce: Option<Nonce>,

    _did: PhantomData<D>,
}

impl<D: Issuer> InvocationBuilder<D, Unset, Unset, Unset, Unset, Unset> {
    /// Creates a blank [`InvocationBuilder`] instance.
    #[must_use]
    pub const fn new() -> Self {
        Self {
            issuer: Unset,
            audience: Unset,
            subject: Unset,
            command: Unset,
            arguments: BTreeMap::new(),
            proofs: Unset,
            cause: None,
            expiration: None,
            issued_at: None,
            meta: BTreeMap::new(),
            nonce: None,
            _did: PhantomData,
        }
    }
}

